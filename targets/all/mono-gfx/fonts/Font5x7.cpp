//! Encodes a 5-pixel row given as a string of '#' (set) and any other char (clear)
constexpr uint8_t R(const char (&s)[6])
{
    return uint8_t(((s[0] == '#') << 7) | ((s[1] == '#') << 6) | ((s[2] == '#') << 5) |
                   ((s[3] == '#') << 4) | ((s[4] == '#') << 3));
}

